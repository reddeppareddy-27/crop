// Get current language
function getCurrentLanguage() {
    return localStorage.getItem('agrismart-lang') || 'en';
}

// Translate content
function translatePage() {
    const currentLang = getCurrentLanguage();
    const trans = translations[currentLang];
    
    if (!trans) return;
    
    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(elem => {
        const key = elem.getAttribute('data-i18n');
        if (trans[key]) {
            if (elem.tagName === 'INPUT' || elem.tagName === 'TEXTAREA') {
                elem.placeholder = trans[key];
            } else {
                elem.textContent = trans[key];
            }
        }
    });
    
    // Update language selector
    const langSelector = document.getElementById('lang-selector');
    if (langSelector) {
        langSelector.value = currentLang;
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLang;
}

// Language switcher function - updated to translate immediately
function setLanguage(lang) {
    localStorage.setItem('agrismart-lang', lang);
    translatePage();
    // No reload needed - translation happens immediately
}

// Initialize on page load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', translatePage);
} else {
    // If DOM is already loaded, translate immediately
    translatePage();
}
